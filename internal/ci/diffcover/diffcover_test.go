func TestParseUnifiedDiff(t *testing.T) {
	t.Parallel()

	t.Run("collects added lines per file across hunks", func(t *testing.T) {
		t.Parallel()

		diff := `diff --git a/internal/foo/foo.go b/internal/foo/foo.go
--- a/internal/foo/foo.go
+++ b/internal/foo/foo.go
@@ -10,2 +10,3 @@ func Foo() {
 context
+added ten plus one
+added ten plus two
@@ -30 +31,2 @@
+thirty one
+thirty two
diff --git a/pkg/bar/bar.go b/pkg/bar/bar.go
--- a/pkg/bar/bar.go
+++ b/pkg/bar/bar.go
@@ -0,0 +1,2 @@
+package bar
+var X = 1
`

		added, err := ParseUnifiedDiff(strings.NewReader(diff))
		require.NoError(t, err)

		assert.Equal(t, []int{11, 12, 31, 32}, added["internal/foo/foo.go"])
		assert.Equal(t, []int{1, 2}, added["pkg/bar/bar.go"])
	})

	t.Run("counts context lines but not removals", func(t *testing.T) {
		t.Parallel()

		diff := `--- a/x.go
+++ b/x.go
@@ -5,3 +5,3 @@
 ctx line five
-removed
+added six
 ctx line seven
`

		added, err := ParseUnifiedDiff(strings.NewReader(diff))
		require.NoError(t, err)
		assert.Equal(t, []int{6}, added["x.go"])
	})

	t.Run("skips deleted files (no post-image)", func(t *testing.T) {
		t.Parallel()

		diff := `--- a/gone.go
+++ /dev/null
@@ -1,3 +0,0 @@
-a
-b
-c
`

		added, err := ParseUnifiedDiff(strings.NewReader(diff))
		require.NoError(t, err)
		assert.Empty(t, added)
	})

	t.Run("rejects malformed hunk headers", func(t *testing.T) {
		t.Parallel()

		diff := `--- a/x.go
+++ b/x.go
@@ nonsense @@
+line
`

		_, err := ParseUnifiedDiff(strings.NewReader(diff))
		require.Error(t, err)
		assert.Contains(t, err.Error(), "malformed hunk header")
	})
}

